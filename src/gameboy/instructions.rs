#[derive(Debug)]
pub enum FlagAffected {
    NotAffected,
    Set,
    Reset,
    Affected,
}
#[derive(Debug)]
pub struct Flags {
    pub zero: FlagAffected,
    pub negative: FlagAffected,
    pub half_carry: FlagAffected,
    pub carry: FlagAffected,
}

#[derive(Debug)]
pub enum OpType {
    Nop,
    Load16,
    Load8,
    Inc16,
    Inc8,
    Dec8,
    Dec16,
    RLCA,
    Add16,
    RRCA,
    Stop,
    RLA,
    Xor,
    Prefix,
    Bit,
    JumpRelative,
    RRA,
    Call,
    Ret,
    Push,
    Pop,
    RL,
    Cp,
    CPL,
    DAA,
    Sub8,
    Add8,
    Jump,
    DI,
    Or,
    EI,
    And,
    Swap,
    RST,
    Res,
    Reti,
    SLA,
    SRL,
    ADC,
    Set,
    Halt,
    RR,
    SBC,
    SCF,
    CCF,
    RLC,
    RRC,
    SRA,
}

#[allow(dead_code)]
#[derive(Debug)]
pub enum FlagsEnum {
    Zero,
    NonZero,
    Subtraction,
    HalfCarry,
    Carry,
    NonCarry,
}

#[derive(Debug)]
pub enum Addressing {
    None,
    Register(Registers),
    RelativeRegister(Registers),
    Immediate16,
    Immediate8,
    Address16,
    RelativeAddress8,
    RelativeAddress16,
    Bit(u8),
    Flag(FlagsEnum),
    RstAddr(u16),
}

#[allow(dead_code)]
#[derive(Debug, Copy, Clone)]
pub enum Registers {
    A,
    F,
    B,
    C,
    D,
    E,
    H,
    L,
    AF,
    BC,
    DE,
    HL,
    HlPlus,
    HlMinus,
    SP,
    PC,
}

#[derive(Debug)]
pub struct Instruction {
    pub opcode: u8,
    pub name: &'static str,
    pub bytes: u8,
    pub cycles: u8,
    pub flags: Flags,
    pub op_type: OpType,
    pub dst: Addressing,
    pub src: Addressing,
}

